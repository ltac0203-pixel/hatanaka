<?php

use Illuminate\Support\Str;

return [

    /*
    |--------------------------------------------------------------------------
    | セッション保存方式を既定化する
    |--------------------------------------------------------------------------
    |
    | リクエストで使う既定のセッション保存方式を指定します。
    | Laravel は複数の保存先に対応しており、DB保存は扱いやすい既定値です。
    |
    | 利用可能: "file", "cookie", "database", "memcached",
    |          "redis", "dynamodb", "array"
    |
    */

    'driver' => env('SESSION_DRIVER', 'database'),

    /*
    |--------------------------------------------------------------------------
    | 放置セッションをいつ失効させるか決める
    |--------------------------------------------------------------------------
    |
    | セッションが未操作のまま有効でいられる分数を指定します。
    | ブラウザ終了時に即失効させたい場合は `expire_on_close` も併用します。
    |
    */

    'lifetime' => (int) env('SESSION_LIFETIME', 1440),

    'expire_on_close' => env('SESSION_EXPIRE_ON_CLOSE', false),

    /*
    |--------------------------------------------------------------------------
    | 保存前に秘匿化するかを決める
    |--------------------------------------------------------------------------
    |
    | 保存前にセッション内容を暗号化するかを指定します。
    | 有効にすると Laravel が自動で暗号化・復号します。
    |
    */

    'encrypt' => env('SESSION_ENCRYPT', true),

    /*
    |--------------------------------------------------------------------------
    | ファイル保存時の置き場を明示する
    |--------------------------------------------------------------------------
    |
    | `file` ドライバー利用時の保存先ディレクトリです。
    | 必要に応じて別の場所へ変更できます。
    |
    */

    'files' => storage_path('framework/sessions'),

    /*
    |--------------------------------------------------------------------------
    | セッションを預ける接続先を揃える
    |--------------------------------------------------------------------------
    |
    | `database` または `redis` ドライバー利用時の接続先を指定します。
    | ここで指定する値は database 設定内の接続名と一致させます。
    |
    */

    'connection' => env('SESSION_CONNECTION'),

    /*
    |--------------------------------------------------------------------------
    | セッション保存先テーブルを明示する
    |--------------------------------------------------------------------------
    |
    | `database` ドライバー利用時にセッションを保存するテーブル名です。
    | 既定値から必要に応じて変更できます。
    |
    */

    'table' => env('SESSION_TABLE', 'sessions'),

    /*
    |--------------------------------------------------------------------------
    | キャッシュ保存時の置き場を揃える
    |--------------------------------------------------------------------------
    |
    | キャッシュベースのセッションドライバーで使う保存先を指定します。
    | ここで指定する値は定義済みのキャッシュストア名と一致させます。
    |
    | 対象: "dynamodb", "memcached", "redis"
    |
    */

    'store' => env('SESSION_STORE'),

    /*
    |--------------------------------------------------------------------------
    | 古いセッションをどの頻度で掃除するか決める
    |--------------------------------------------------------------------------
    |
    | 一部ドライバーでは古いセッションを手動で掃除する必要があります。
    | ここでは各リクエストで掃除処理が走る確率を指定します。
    |
    */

    'lottery' => [2, 100],

    /*
    |--------------------------------------------------------------------------
    | セッション識別子の名前を決める
    |--------------------------------------------------------------------------
    |
    | フレームワークが発行するセッションクッキー名を変更できます。
    | ただし変更しても大きな安全性向上にはつながらないことが多いです。
    |
    */

    'cookie' => env(
        'SESSION_COOKIE',
        Str::slug((string) env('APP_NAME', 'laravel')).'-session'
    ),

    /*
    |--------------------------------------------------------------------------
    | クッキーを有効にするURL範囲を決める
    |--------------------------------------------------------------------------
    |
    | クッキーを有効とみなすパスを指定します。
    | 通常はアプリケーションのルートパスを使います。
    |
    */

    'path' => env('SESSION_PATH', '/'),

    /*
    |--------------------------------------------------------------------------
    | クッキーを有効にするドメイン範囲を決める
    |--------------------------------------------------------------------------
    |
    | クッキーを有効にするドメインとサブドメイン範囲を指定します。
    | 通常は既定値のままで問題ありません。
    |
    */

    'domain' => env('SESSION_DOMAIN'),

    /*
    |--------------------------------------------------------------------------
    | 安全な通信経路でだけクッキーを送る
    |--------------------------------------------------------------------------
    |
    | 有効にすると HTTPS 接続時だけセッションクッキーを送信します。
    | 安全に送れない経路へクッキーが流れることを防ぎます。
    |
    */

    'secure' => env('SESSION_SECURE_COOKIE', str_starts_with((string) env('APP_URL', ''), 'https')),

    /*
    |--------------------------------------------------------------------------
    | JavaScript からの読み取りを防ぐ
    |--------------------------------------------------------------------------
    |
    | 有効にすると JavaScript からクッキー値へアクセスできなくなります。
    | 通常は無効化しない方が安全です。
    |
    */

    'http_only' => env('SESSION_HTTP_ONLY', true),

    /*
    |--------------------------------------------------------------------------
    | CSRF を抑える送信ルールを決める
    |--------------------------------------------------------------------------
    |
    | クロスサイトリクエスト時のクッキー送信挙動を指定します。
    | CSRF 緩和に関わるため、既定では安全寄りの `lax` を使います。
    |
    | 参考: https://developer.mozilla.org/en-US/docs/Web/HTTP/Headers/Set-Cookie#samesitesamesite-value
    |
    | 利用可能: "lax", "strict", "none", null
    |
    */

    'same_site' => env('SESSION_SAME_SITE', 'lax'),

    /*
    |--------------------------------------------------------------------------
    | クロスサイト利用時の分離方法を決める
    |--------------------------------------------------------------------------
    |
    | 有効にするとクロスサイト文脈でもトップレベルサイト単位でクッキーを分離します。
    | 利用には `secure` と `SameSite=none` の指定が必要です。
    |
    */

    'partitioned' => env('SESSION_PARTITIONED_COOKIE', false),

];
